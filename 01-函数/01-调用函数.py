
# help()方法可以查看函数的帮助信息
help(abs)

# abs() 求绝对值
print(abs(-88))
print(abs(88))

# 数据转换函数：int()、float()、str()、bool()
var = '123'
print(int(var))
var = '23.3'
print(float(var))
var = 123
print(str(var))
var = 1
print(bool(var))
var = 0
print(bool(var))
var = ''
print(bool(var))

# hex():把一个整数转为十六进制表示的字符串
n = 255
print(hex(n))
